bool m_string[1024][8][7];

void make_string( char* c_string ) {
	for( int i = 0; i < strlen( c_string ); i++ ) {
		if( c_string[i] - 96 >= 0 && c_string[i] - 97 < 26 ) {
			memcpy( m_string[i], glyphs[c_string[i] - 97], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == ',' ) {
			memcpy( m_string[i], glyphs[26], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '.' ) {
			memcpy( m_string[i], glyphs[27], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '#' ) {
			memcpy( m_string[i], glyphs[28], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '[' ) {
			memcpy( m_string[i], glyphs[29], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == ']' ) {
			memcpy( m_string[i], glyphs[30], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '\'' ) {
			memcpy( m_string[i], glyphs[31], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '-' ) {
			memcpy( m_string[i], glyphs[32], sizeof(bool) * 8 * 7 );
		}
		else if( c_string[i] == '~' ) {
			memcpy( m_string[i], glyphs[33], sizeof(bool) * 8 * 7 );
		}
		else {
			memset( m_string[i], 0, sizeof(bool) * 8 * 7 );
		}
	}
}

bool is_in_string( u32 x, u32 y ) {
	u8 idx_x = (x / 4)%7;
	u32 idx_y = (y / 4);
	if( idx_y >= 8 ) {
		return( false );
	}
	u16 idx_pos = (x / 4)/7;
	if( m_string[idx_pos][idx_y][idx_x] == 1 ) {
		return( true );
	}
	else {
		return( false );
	}
}
